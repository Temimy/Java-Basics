package testing;

public class Switch_String {

	public static void main(String[] args) {

		String value = "Bedn";

		switch (value) {
		case "Alaa": {

			System.out.println("This is Alaa");
			break;
		}
		case "Bedn": {

			System.out.println("This is Bedn");
			break;
		}
		case "Temimy": {

			System.out.println("This is Temimy");
			break;
		}

		default:
			System.out.println("Non");
			break;
			
	
		}

	}

}
