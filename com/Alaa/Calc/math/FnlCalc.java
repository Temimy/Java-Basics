package com.Alaa.Calc.math;

public class FnlCalc {

	// Add values
	public static double addValue(double na, double nb) {
		double result = na + nb;
		return result;
	}

	// Sub values
	public static double subValue(double n1, double n2) {
		double result = n1 - n2;
		return result;
	}

	// Mult values
	public static double multValue(double n1, double n2) {
		double result = n1 * n2;
		return result;
	}

	// Div values
	public static double divValue(double n1, double n2) {
		double result = n1 / n2;
		return result;
	}

}
